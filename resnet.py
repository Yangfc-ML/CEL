class CWENet(nn.Module):

    def __init__(self, args):
        super().__init__()
        model_fun, dim_in = model_dict[args.model]

        if args.ds not in ['cifar100', 'cifar100H']:
            model = ResNet34_Without_AveragePooling(models.resnet.BasicBlock, [3, 4, 6, 3])
            model.load_state_dict(models.resnet34(pretrained=True).state_dict())
            self.encoder = model
        else:
            self.encoder = model_fun()

        self.class_encoder = CLASS_WISE_ENCODER(num_classes=args.num_classes, initial_num_features=dim_in,
                                           num_of_groups=args.num_classes, decoder_embedding=args.embed_dim)
        self.register_buffer("class_wise_prototypes", torch.zeros(args.num_classes, args.embed_dim))

    def update_prototypes(self, feature_w, logits_w, candidate_label):
        feature_w = feature_w.clone().detach()
        logits_w = logits_w.clone().detach()
        candidate_label = candidate_label.clone().detach()
        logits_w = logits_w * candidate_label

        value, index = torch.max(logits_w, 1)
        for num in range(len(feature_w)):
            self.class_wise_prototypes[index] = self.class_wise_prototypes[index[num]] + feature_w[num][index[num]]
        self.class_wise_prototypes = F.normalize(self.class_wise_prototypes, dim=1)

    def forward(self, x_w, x_s=None, Y=None, eval_only=False):

        feature_w = self.encoder(x_w)
        feature_w, logits_w = self.class_encoder(feature_w)

        if eval_only:
            return feature_w, logits_w

        feature_s = self.encoder(x_s)
        feature_s, logits_s = self.class_encoder(feature_s)

        self.update_prototypes(feature_w, logits_w, Y)

        return logits_w, logits_s, feature_w, feature_s
